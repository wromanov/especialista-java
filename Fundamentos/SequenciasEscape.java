package Fundamentos;

public class SequenciasEscape {
    public static void main(String[] args) {

        //Usasse "\" para escapar as duplas.
        System.out.println("Oi \"Maria\"");

        //Pular linha "\n"
        System.out.print("Ola meus\namigos!\n");

        //Escapar a "barra" usa-se duas barras.
        System.out.println("C:\\Windows\\System");





    }
}
